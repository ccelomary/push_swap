#include "print.h"

long		ft_atoi(char **s)
{
	long	result;
	long	sign;

	sign = 1;
	result = 0;
	if (**s == '-')
		sign = -1;
	while (**s >= '0' && **s <= '9')
	{
		result = (result * 10 + **s - 48);
		(*s)++;
	}
	return (result * sign);
}
